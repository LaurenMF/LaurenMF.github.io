let box =  document.querySelector("#box");

function change() {
    box.style.backgroundColor = "#000000";
    box.style.height = "100px";
    box.style.width = "100px";
} 

function back() {
    box.style.backgroundColor = "#0000ff";
    box.style.height = "100px";
    box.style.width = "100px";
}